package While반복문;
import java.util.Scanner;
public class WhileEx1 {
    public static void main(String[] args) {
        int treeHit = 0;
        while(treeHit < 10) {
            treeHit++;
            System.out.println("나무를 " + treeHit + "번 찍었습니다.");
            if(treeHit == 10) {
                System.out.println("나무가 넘어 갑니다.");
            }
        }
        for(int i = 1; i <= 10; i++) {
            System.out.println("나무를 " + i + "번 찍었습니다.");
        }
        System.out.println("나무가 넘어 갑니다.");
    }
}
